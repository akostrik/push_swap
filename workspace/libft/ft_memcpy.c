// Copies n bytes from memory area src to memory area dest 
// The memory areas must not overlap
// Returns a pointer to dest
// ft_memcpy       : 1.OK 2.OK 

#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		*((char *)dest + i) = *((char *)src + i);
		i++;
	}
	return (dest);
}
